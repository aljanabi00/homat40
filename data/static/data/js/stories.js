function stories_show() {
    var show = document.getElementById("read_sec");
    var hide = document.getElementById("watch_sec");
    var hide2 = document.getElementById("listen_sec");
    var bg_color = document.getElementById("read_icon");
    var bg_color2 = document.getElementById("watch_icon");
    var bg_color3 = document.getElementById("listen_icon");
    var hover = document.getElementById("type_icon");

    show.style.display = "block";
    hide.style.display = "none";
    hide2.style.display = "none";
    bg_color.style.background = "linear-gradient(to left, #d86800, #e7a600)";
    bg_color2.style.background = "#505050";
    bg_color3.style.background = "#505050";
    hover.style.hover.background = "linear-gradient(to left, #d86800, #e7a600)"
}

